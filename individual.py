def find_min_recursive(x, start_index):
    """
    Рекурсивная функция для нахождения минимального элемента списка.

    Аргументы:
    - x (list): Список вещественных чисел.
    - start_index (int): Индекс, с которого начинается поиск минимума.

    Возвращает:
    - float or None: Минимальный элемент списка x или None, если список пуст.
    """
    # Если start_index достиг конца списка, возвращаем текущий элемент
    if start_index == len(x) - 1:
        return x[start_index]
    else:
        # Рекурсивно находим минимум в оставшейся части списка
        rest_min = find_min_recursive(x, start_index + 1)
        # Возвращаем минимум между текущим элементом и оставшимся минимумом
        return min(x[start_index], rest_min)


def find_min(x):
    """
    Функция для нахождения минимального элемента списка, вызывая вспомогательную рекурсивную функцию.

    Аргументы:
    - x (list): Список вещественных чисел.

    Возвращает:
    - float or None: Минимальный элемент списка x или None, если список пуст.
    """
    # Если список пуст, возвращаем None
    if not x:
        return None
    else:
        # Вызываем вспомогательную рекурсивную функцию с начальным индексом 0
        return find_min_recursive(x, 0)


# Пример использования
x = [3.5, 1.2, 5.7, 2.1, 4.8]
min_element = find_min(x)
print(f"Минимальный элемент списка x: {min_element}")